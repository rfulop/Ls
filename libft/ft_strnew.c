#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*res;

	res = NULL;
	if (!(res = (char*)malloc(sizeof(char) * size + 1)))
		return (NULL);
	ft_bzero(res, size + 1);
	return (res);
}
